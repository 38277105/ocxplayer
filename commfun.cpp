void *CommonFun::grow_array(void *array, int elem_size, int *size, int new_size)
{
    if (new_size >= INT_MAX / elem_size) {
        av_log(NULL, AV_LOG_ERROR, "Array too big.\n");
        exit(1);
    }
    if (*size < new_size) {
        uint8_t *tmp = (uint8_t *)av_realloc_array(array, new_size, elem_size);
        if (!tmp) {
            av_log(NULL, AV_LOG_ERROR, "Could not alloc buffer.\n");
            exit(1);
        }
        memset(tmp + *size*elem_size, 0, (new_size-*size) * elem_size);
        *size = new_size;
        return tmp;
    }
    return array;
}
int CommonFun::get_max_dev(int n1,int n2)
{
    int nDev = min(n1,n2);
    int nDiv = max(n1,n2);
    int nMod = nDiv % nDev;
    while(0 != nMod)
    {
        nDiv = nDev;
        nDev = nMod;
        nMod = nDiv % nDev;
    }
    return nDev;

}



















